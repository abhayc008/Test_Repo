package basic_programs;

public class RealationalOperator
{
	public static void main(String args[] ) 
	{
		int no1=90, no2=65;
		boolean check;
		
		check=no1>no2;
		
		System.out.println(check);
		System.out.println(no1<no2);
		System.out.println(no1>no2);
		System.out.println(no1<=no2);
		System.out.println(no1==no2);
		System.out.println(no1>=no2);
		System.out.println(no1!=no2);
		
	}

}
